package com.src1;

import java.util.PriorityQueue;

public class PriorityQueue25 {

	public static void main(String[] args) {
		PriorityQueue pq=new PriorityQueue();
		pq.add("Red");
		pq.add("Orange");
		pq.add("Black");
		pq.add("White");
		pq.add("Blue");
		System.out.println(pq);
		String str=pq.toString();
		System.out.println(str);

	}

}
